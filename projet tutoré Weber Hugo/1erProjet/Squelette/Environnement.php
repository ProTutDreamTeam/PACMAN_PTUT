    <p>La pollution engendrée par l'utilisation des différents carburants a base de pétrole
       tels que le diesel ont de nombreux effets dévastateurs nottament sur la santé des consommateurs.
       En effet il à été prouvé que les moteurs diesels émettent essentiellement des particules ultrafines,
       bien connu pour leur toxicités. Meme les moteurs diesel dernière génération relachent de ces particules
       selon Thomas Bourdrel, radiologue et fondateur du collectif Strasbourg respire. Ces particules ultrafines touchent
       tout le monde et représentent uniquement l'un des nombreux autres éléments nocifs a la santé, tel le CO2 ou encore le
       dioxyde d'azote, qui sont relachés dans l'air quand des moteurs sont en marches. L'un des majeurs effet de la pollution
       atmosphérique du a l'utilisation de véhicule fonctionnent à l'essence est la formation de nuages de pollution appelés
       smog. Ces nuages de pollution ont des impacts désastreux sur la santé et sont déjà responsable de plusieur millions de décès</p>
       <br>
    <p>Le transport routier est le principales moyens de déplacement dans notre société, en effet tout le monde l'utilise au moins une
      fois par jour dans le but d'allez travailler ou de rentrer chez soi. Cependant cette utilisation massive de véhicule fonctionnent
      aux énergies fossiles a des éffets néfastes sur l'environnement. On peut citer par exemple le rejet de particule nuisant à la couche
      d'ozone tel que le CO2 (dioxyde de carbone) ou encore l'émanations de gaz à effets de serre dont le secteur routier représente à lui
      seul 30 % des émissions de gaz a effets de serre dans l'Hexagone, le trafic représentant a lui seul 95% de ce pourcentage. Cette pollution
      continue de l'air à été la responsable de la formation de plusieur catastrophe naturel dont les smog, des brumes brunatre épaissent
      provenant de mélanges de polluants atmosphériques. Ces nuages de pollution ont des effets néfastes sur l'environnement mais également
      sur la santé des humains et ont la facheuse tendances a disparaitre lentement. Ce type de désastre est de moins en moins rare a travers
      le monde et si nous ne changeons pas nos habitudes, il se pourrait que nous passions notre vie entière avec un masque sur le visage.</p>
    <br>
    <p>La production et l’exploitation des différents carburants fonctionnant à l’énergie fossile est l’une des industries les plus développé du monde.
       Pourtant cette industrie a des effets catastrophique sur l’environnement provoquant parfois la destruction complète de la biodiversité d’une région.
       Or cette destruction de la biodiversité à différents effets à long terme, tous ayant un impacts très négatif sur l’environnement, et la plupart
       résulte de l’extraction des énergies fossile tels que le charbon, le pétrole ou encore les gazs naturels. On peut prendre alors l’exemple de
       l’exploitation des sables bitumineux d’Athabasca au Canada, exploitation déjà très médiatisé. Ce simple forage visant a trouvé du pétrole a entraîné
       la disparition des peuples autochtones de la région, la libérations d’émanations de CO2 dans l’air et la destruction de l’environnement de la régions.
       Ce genre d’incident est très répandus dans le monde et ne va malheureusement pas diminuer tant que l’humanité sera dépendantes de ces carburants.</p>
